SELECT TOP 1 (SELECT AVG(PROGRESS.NOTE) FROM PROGRESS WHERE PROGRESS.SUBJECT = 'ОАиП')[ОАиП],
       (SELECT AVG(PROGRESS.NOTE) FROM PROGRESS WHERE PROGRESS.SUBJECT = 'БД')[БД],
	   (SELECT AVG(PROGRESS.NOTE) FROM PROGRESS WHERE PROGRESS.SUBJECT = 'СУБД')[СУБД]
FROM PROGRESS

